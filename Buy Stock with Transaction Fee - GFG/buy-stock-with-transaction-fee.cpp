class Solution{
    public:
    long long maximumProfit(vector<long long>&prices, int n, int fee) 
    {
       long long buy = 0, sell = 0;
        
        for(long long index = n-1;index>=0;index--)
        {
            buy = max(-prices[index]+ sell,buy);
            sell =  max(prices[index]-fee+buy,sell);
        }
        
        return buy;
    }
};

//{ Driver Code Starts.
int main()
{
    int t;
    cin>>t;
    while(t--){
        int n; cin>>n;
        vector<long long> prices;
        
        for(int i=0; i<n; ++i){
            long long x; cin>>x;
            prices.push_back(x);
        }
        
        int fee; cin>>fee;
        
        Solution obj;
        cout<<obj.maximumProfit(prices, n, fee)<<"\n";
    }
    return 0;
}
